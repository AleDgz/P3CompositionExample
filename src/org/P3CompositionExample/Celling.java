package org.P3CompositionExample;

public class Celling {
	
	//fields
	private int heigh;
	private int paintedColor;

	//constructor
	public Celling(int heigh, int paintedColor) {
		super();
		this.heigh = heigh;
		this.paintedColor = paintedColor;
	}

	public int getHeigh() {
		return heigh;
	}

	public int getPaintedColor() {
		return paintedColor;
	}
	
	
	
}
